# Belajar Variable
nama = "Andi"  # Contoh tipe data string (str)
umur = 17  # Contoh tipe data integer (int)
nilai = 95  # Nilai

# Tanda koma ',' pada print digunakan untuk menambahkan nilai variabel ke perintah print.
print("Dia bernama ", nama) 
print("Dia berusia ", umur, " tahun")
print("Dia mendapatkan nilai ", nilai)
print("Oleh karena itu, Dinyatakan LULUS")

